            <div class="row">
                <div class="col-md-5">
                    <p class="big-p">What are NFTs?</p>
                </div>
                <div class="col-md-7 small-p secondary-color">
                    <p>An NFT (non-fungible token) is a digital certificate for intellectual property recorded on a distributed ledger, such as a blockchain like Ethereum. NFTs can be anything that is digital, from collectibles and art pieces to music and even short movie segments ‚Äî the possibilities are endless. ‚ÄúNon-fungible,‚Äù more or less, means that an NFT is unique and cannot be replaced with something else. A Bitcoin is an example of a fungible token, which means you can trade one Bitcoin for another identical Bitcoin and you'll have exactly the same thing. NFTs are unique and thus non-fungible.</p>
                    <p>For a more comprehensive explanation:</p>
                    <p><a href="https://opensea.io/blog/guides/non-fungible-tokens/">https://opensea.io/blog/guides/non-fungible-tokens/</a></p>
                    <img src="/media/wtf/rtfkt-nft.png" alt="">
