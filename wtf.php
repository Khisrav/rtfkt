                    <p>Wallets are created and secured with a private seed phrase, and function similar to a browser extension (e.g. on Chrome, Firefox, Brave) that allows you to access DApps (decentralized applications) and perform crypto or NFT transactions. MetaMask is the most popular wallet currently in use in the NFT space.</p>
