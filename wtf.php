                    <p class="big-p">How do I keep my wallet safe and secure?</p>
                </div>
                <div class="col-md-7 small-p secondary-color">
                    <p>A wallet created solely on your browser (e.g. MetaMask) is known as a ‚Äúhot wallet‚Äù. A hot wallet is a digital wallet that is kept online and can be accessed from any device if you have internet and your private keys. For hot wallets, private keys are stored and encrypted on the app itself which is online. Because hot wallets are online, they are easier to use for regular transactions, but they tend to be less secure and are subject to hacking and theft.</p>
                    <p>We recommend getting a ‚Äúcold‚Äù hardware wallet (e.g. Ledger or Trezor) which adds another layer of security. A hardware wallet is a type of ‚Äúcold wallet‚Äù that is a physical device, typically a USB. In general, a cold wallet is a wallet that is stored offline. Since cold wallets aren‚Äôt connected to the internet, they are less vulnerable to online hacks or thefts compared to a hot wallet. Some hardware wallets will provide you access to multiple Dapps, such as Metamask, and perform transactions as usual, with an added step of confirmation on the hardware wallet token physical device.</p>
                    <p>Regardless, best practices include:</p>
                    <ul>
                        <li>Never share your seed phrase or store it on your computer</li>
                        <li>Do not click on suspicious links you do not recognise, and</li>
                        <li>Store your precious assets on the ‚Äúcold wallet‚Äù while using the ‚Äúhot wallet‚Äù to perform transactions.</li>
