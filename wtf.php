                    <p class="big-p">What are gas fees?</p>
                </div>
                <div class="col-md-7 small-p secondary-color">
                    <p>Gas fees are the transaction fees that users pay to validators on a blockchain protocol to have their transactions included in a block. The system works on a standard supply and demand mechanism. If there is more demand for transactions, validators can choose to include the transactions that pay more, compelling users to pay more to have their transactions processed quickly and efficiently. Users of Ethereum can also choose to pay more for faster transactions, by paying higher gas fees. Wallets like MetaMask enable users to interact directly with the Ethereum network, choosing which amount of gas they wish to pay. There are several websites where you can track gas prices, like <a href="https://ethgastation.info/">https://ethgastation.info/</a> or <a href="https://gasnow.org">https://gasnow.org</a>.</p>
